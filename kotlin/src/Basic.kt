fun main(args: Array<String>){

    println("정수: ${100}")
    println("정수 (Long): ${100L}")
    println("실수 : ${55.55}")
    println("실수 (Float) : ${55.55f}")
    println("정수 (Long): ${100_000L}")



    var a1 = 100
    println("a1: ${a1}")

    var a3 : Int = 100;

    println("a3: ${a3}")

    val a4 : String = "str";

    println("a4: ${a4}")

}

